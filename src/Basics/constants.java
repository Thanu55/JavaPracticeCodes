package Basics;

public class constants {
    public static void main(String[] args) {
        /* CONSTANTS
        A constant is a variable in Java which has a fixed value i.e. it cannot be assigned a different value
        once assigned. For example value of Pi
         */
        // using the keyword "final" this can be achieved
        // Using of Upper case for variable will define usually it's a constant value
        final float PI = 3.14F;
        System.out.println(PI);
    }
}
